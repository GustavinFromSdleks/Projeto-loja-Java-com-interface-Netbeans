    private void filtrarTabela() {
    // Obter o texto do campo de filtro
    String textoFiltro = JTFfiltro.getText();

    // Criar um filtro de expressão regular para todas as colunas
    RowFilter<DefaultTableModel, Object> filtro = RowFilter.regexFilter("(?i)" + textoFiltro);

    // Obter o modelo da tabela
    DefaultTableModel modelo = (DefaultTableModel) JTableCarrinho.getModel();

    // Criar um classificador de linhas para o modelo da tabela
    TableRowSorter<DefaultTableModel> sorter = new TableRowSorter<>(modelo);

    // Configurar o filtro para todas as colunas
    sorter.setRowFilter(filtro);

    // Configurar o classificador de linhas na tabela
    JTableCarrinho.setRowSorter(sorter);
}

    
      
 
