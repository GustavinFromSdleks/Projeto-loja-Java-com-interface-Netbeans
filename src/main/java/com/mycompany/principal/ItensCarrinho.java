    private void JBTexcluirDoCarrinhoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBTexcluirDoCarrinhoActionPerformed
     String auxString;
        
    if (JTableCarrinho.getSelectedRow() != -1) { 
        aux = JOptionPane.showConfirmDialog(null, "Deseja Realmente Excluir o produto?", "Excluir Produto", JOptionPane.YES_NO_OPTION);
        if (aux == 0) {
                       
            DefaultTableModel modelo = (DefaultTableModel) JTableCarrinho.getModel();
            auxString = (String) modelo.getValueAt(JTableCarrinho.getSelectedRow(), 0);   // para referenciar minha coluna nao coloco seu nome, e sim seu numero
            modelo.removeRow(JTableCarrinho.getSelectedRow()); //ps: O (String) em frente a linha serve para converter meu Objeto recebido para String.
                        
            
            for(Roupa roupa : unicCar){
                if (roupa.getNome().equals(auxString)) {
                    unicCar.removerRoupa(roupa);
                    break; // removendo roupa do meu banco
                }
            }
            
             if (auxString.equals("Camiseta")) {
                Camiseta CT = new Camiseta("a", "b", "c", "d", 100);
                CT.aumentarCamisetaDisponivel();
                
            } else if (auxString.equals("Calca")) {
                Calca CL = new Calca("a", "b", "c", "d", 80);
                CL.aumentarCalcaDisponivel();
                
            } else if (auxString.equals("Meia")) {
               Meia ME = new Meia("a", "b", "c", "d", 20);
               ME.aumentarMeiaDisponivel();
            }
            
            
        }            
    }else {
        JOptionPane.showMessageDialog(null, "Selecione um produto para excluir");
    }   
       
    }//GEN-LAST:event_JBTexcluirDoCarrinhoActionPerformed

    
    
